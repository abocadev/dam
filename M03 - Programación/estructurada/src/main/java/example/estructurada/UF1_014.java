package example.estructurada;

import java.util.Scanner;

public class UF1_014 {
    public static void main(String[] args) {
        Scanner input = new Scanner (System.in);
        int num = input.nextInt();
        int i = 1;
        while (i <= num){
            System.out.println(i);
            i++;
        }
    }
    
}
