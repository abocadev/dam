package example.estructurada;

import java.util.Scanner;

public class UF1_015 {
    public static void main(String[] args) {
        Scanner input = new Scanner (System.in);
        int num = input.nextInt();
        int i = 1;
        do{
            System.out.println(i);
            i++;
        }while (i <= num);
    }
    
}
